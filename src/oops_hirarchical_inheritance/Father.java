package oops_hirarchical_inheritance;

public class Father { //super class
	
	public void home() {
		System.out.println("home");
	}
	public void car() {
		System.out.println("car");
	}
	public void money() {
		System.out.println("money");
	}

}
//multiple subclasses aquire/access properties of one super class
//called Hirarchical Inheritance